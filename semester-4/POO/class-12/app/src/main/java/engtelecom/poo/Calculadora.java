package engtelecom.poo;

/**
 * Esca classe representa uma caluladora;
 * 
 */
public class Calculadora {

  /**
   * 
   * Este metodo realiza uma soma;
   * 
   * @param a O parâmetro "a" e correspondente ao primeiro número da soma;
   * @param b O parâmetro "b" e correspondente ao segundo número da soma;
   * @return retorna o valor da soma dos dois operandos.
   */

  public int somar(int a, int b) {

    return (a + b);
  }

  public int subtrair(int a, int b) {

    return (a - b);
  }

}
